import openai

client = openai.OpenAI(api_key="sk-...") ##open api key here

client_input = """
Our client is a mid-sized logistics company operating in the Midwest. Their core challenge is inefficiency in last-mile delivery due to outdated routing software and lack of real-time tracking. They want to expand into two new states within 6 months while reducing current delivery costs by at least 10%. Competitors have already implemented AI-based route optimization.
"""

prompt = f"""
You are a management consultant. Analyze the following client brief and provide:
1. A 3-sentence executive summary
2. 5-point strategy recommendation
3. Bullet summary for a PowerPoint slide

Client Input: {client_input}
"""

response = client.chat.completions.create(
    model="gpt-4",
    messages=[
        {"role": "user", "content": prompt}
    ],
    temperature=0.7,
    max_tokens=700
)

print(response.choices[0].message.content)

